const DOCS = { privacy: PRIVACY, consent: CONSENT };

export default function LegalModal({ doc, onClose }) {
  useEffect(() => {
    const onKey = (e) => e.key === 'Escape' && onClose();
    document.addEventListener('keydown', onKey);
    document.documentElement.style.overflow = 'hidden';
    return () => {
      document.removeEventListener('keydown', onKey);
      document.documentElement.style.overflow = '';
    };
  }, [onClose]);

  const data = DOCS[doc];
  if (!data) return null;

  return (
    <div className="legal-overlay" onClick={onClose}>
      <div className="legal-modal" role="dialog" aria-modal="true" onClick={(e) => e.stopPropagation()}>
        <div className="legal-head">
          <h3>{data.title}</h3>
          <button className="legal-close" onClick={onClose} aria-label="Закрыть">×</button>
        </div>
        <div className="legal-body">
          {data.body.map(([h, p], i) => (
            <div className="legal-section" key={i}>
              {h && <h4>{h}</h4>}
              <p>{p}</p>
            </div>
          ))}
        </div>
      </div>
    </div>
  );
}
